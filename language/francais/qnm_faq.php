<b>Un br&egrave;ve introduction</b><br/>
<div style="margin:15px">
<a id="I1"></a><b>Qu'est-ce qu'un &eacute;l&eacute;ment du r&eacute;seau ?</b><br/>
Un &eacute;l&eacute;ment est un &eacute;quipement int&eacute;gr&eacute; dans le r&eacute;seau. Notez qu'un &eacute;l&eacute;ment peut aussi &ecirc;tre un groupe d'&eacute;quipements comme une station, un rack, ... 
Par d&eacute;finition un &eacute;l&eacute;ment peut contenir des sous-&eacute;l&eacute;ments.<br/>
<br/>
<a id="I2"></a><b>Qu'est-ce qu'un connecteur ?</b><br/>
Comme son nom l'indique, un connecteur est ce qui permet de connecter les &eacute;l&eacute;ments entre eux. 
Notez que les connecteurs sont obligatoirement des sous-&eacute;l&eacute;ments d'un &eacute;quipement et qu'ils ne peuvent pas avoir eux-m&ecirc;me des sous-&eacute;l&eacute;ments.
Il n'est pas obligatoire de d&eacute;tailler les connecteurs de tous les &eacute;quipements pour pouvoir constituer un r&eacute;seau. 
QNM permet aussi de d&eacute;finir des relations entre les &eacute;quipements sans d&eacute;tailler tous les connecteurs.<br/>
<br/>
<a id="I3"></a><b>Qu'est-ce qu'une ligne ?</b><br/>
Une ligne est un &eacute;l&eacute;ment (c&acirc;ble, route, tuyaux) qui peut contenir des doubles connecteurs : un &agrave; chaque extr&eacute;mit&eacute; de la ligne.
Lors de la cr&eacute;ation de connecteurs sur une ligne, QNM proposera la cr&eacute;ation automatique des connecteurs correspondants (appel&eacute;s miroirs) pour l'autre extr&eacute;mit&eacute; de la ligne.
Une ligne (ex: un c&acirc;ble) permet de relier des &eacute;quipements entre eux. 
Il est possible, mais pas obligatoire, de d&eacute;crire les relations au niveau de chaque connecteurs (sur les &eacute;quipements et aux deux extr&eacute;mit&eacute;s de la ligne).<br/>
<br/>
<a id="I4"></a><b>Gestion du r&eacute;seau</b><br/>
QNM permet la gestion du r&eacute;seau (&eacute;quipements, connecteurs, lignes, et leur relations). 
Notons que le point important de QNM est de permettre une gestion collaborative de ces &eacute;l&eacute;ments. 
Les &eacute;l&eacute;ments peuvent &ecirc;tre document&eacute;s (photos, fiches techniques) et QNM permet de g&eacute;rer les remarques (notes) que les utilisateurs rapportent.<br/>
