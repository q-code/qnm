<a id="L1"></a><b>Waarom moet ik registreren?</b><br/>
Registratie geeft je toegang tot bijkomende functies die voor gasten niet beschikbaar zijn. Het registreren bestaat over het algemeen uit het invullen van je naam, e-mail en wachtwoord, de overige gegevens zijn optioneel. Het registreren neemt dus niet veel tijd in beslag.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="L2"></a><b>Hoe kan ik registreren?</b><br/>
In de registratiepagina zult u het gebruiksbeleid vinden. Lees het zorgvuldig. Als u met de voorwaarden het akkoord gaat, kunt u uw gebruikersbenaming kiezen. Afhankelijk van de registratieveiligheid die door de Beheerder wordt bepaald zult u direct uw wachtwoord kiezen en het moeten bevestigen, OF uw e-mailadres  ingaan (een tijdelijk wachtwoord zal verzendt naar u zijn). U zult ook een code moeten kopi&euml;ren die in de vorm wordt getoond. Als de gebruikersbenaming u kiest al door een raadslid wordt gebruikt, zult u een andere gebruikersbenaming moeten kiezen.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="L3"></a><b>Kan ik mijn E-mail verbergen?</b><br/>
In uw Profiel zult u een privacyoptie vinden om uw e-mail voor niet geregistreerde bezoekers of voor leden te verbergen.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="L4"></a><b>Ik ben mijn wachtwoord kwijt!</b><br/>
Geen paniek! De wachtwoorden kunnen niet opgehaald worden, wel kan er een nieuw wachtwoord aangemaakt worden. Om dit te doen ga je naar de Inloggen pagina en daar klik je op <u>Wachtwoord vergeten</u>, volg hierna de instructies, er wordt een wachtwoord opgestuurd naar het email adres dat in je profiel staat.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="L5"></a><b>Ik was in het verleden geregistreerd maar kan niet meer inloggen!</b><br/>
De meest aannemelijke redenen hiervoor zijn; je hebt een onjuiste gebruikersnaam of wachtwoord ingegeven (kijk de e-mail die je toen bij registratie hebt gekregen) of de beheerder heeft uw account gewist voor enkele bepaalde redenen. Je hebt misschien nooit iets gepost? Het is gebruikelijk om periodiekz gebruikers te wissen die niets hebben gepost om de ruimte van de database te beperken. Probeer opnieuw te registreren en doe mee aan de discussies.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="L6"></a><b>Ik ben geregistreerd maar kan niet inloggen!</b><br/>
Hebt u geregistreerd? Ernstig, moet u registreren in te loggen. Bent u verboden van het systeem ? (Zal het a- bericht worden getoond als u hebt) Als zo, zou u de webmaster of administratoren moeten contacteren om waarom te weten te komen. Als u geregistreerd bent ( en niet verboden) en u nog niet inloggen kunt, controleer uw gebruikersbenaming en wachtwoord dubbel. Gewoonlijk is dit het probleem; als niet, contacteer de administratoren.
