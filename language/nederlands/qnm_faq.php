<a id="U1"></a><b>Wie zijn Administratoren?</b><br/>
De Administratoren (beheerders) zijn mensen toegewezen het hoogste niveau van controle over de volledige. Deze mensen kunnen alle facetten van systeem instellingen controleren die plaatsende toestemmingen omvatten, verbiedend gebruikers, die staffleden cre&euml;ren, die de sectie veiligheid, de stijl, enz. controleren. Zij hebben ook volledige staffleden mogelijkheden in alle secties.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="U2"></a><b>Wie zijn Moderatoren?</b><br/>
De moderatoren zijn individuen van wie baan het is om de activiteit van de secties van dag tot dag te zorgen. Zij hebben de bevoegdheid om posten uit te geven of te schrappen en, elementen activeren of inactiveren, post te heropenen te bewegen en te schrappen dat zij zich hebben gematigd.
