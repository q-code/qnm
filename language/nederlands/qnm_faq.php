<a id="P2"></a><b>Hoe geef schrap ik of een post uit ?</b><br/>
Tenzij u de van de systeem beheerder of stafflid bent kunt u uw eigen posten slechts uitgeven of schrappen. U kunt een post uitgeven door te klikken uitgeeft knoop voor de relevante post. Als iemand reeds heeft geantwoord op de post, zult u een klein stuk van tekstoutput onder de post vinden; het zal ook verschijnen als de staffleden of de beheerders de post uitgeven (zij zouden een bericht moeten verlaten zeggend wat zij veranderden en waarom). Gelieve te merken op dat de normale gebruikers een post niet kunnen schrappen zodra iemand heeft geantwoord.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="P3"></a><b>Hoe voeg ik een handtekening aan toe mijn post?</b><br/>
Om een handtekening aan een post toe te voegen moet u eerst tot stand brengen; dit wordt gedaan via uw profiel. Wanneer een handtekening wordt gecre&euml;erd, zal het met uw bericht worden getoond. Om uw handtekening te verbergen, verander het enkel in een lege tekst in uw profiel.
<br/>
