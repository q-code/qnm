<a id="P4"></a><b>Waarom kan ik niet tot een sectie toegang hebben?</b><br/>
De administrator kan beslissen de toegang voor niet geregistreerde gebruikers te beperken. In dit geval, slechts zullen de geregistreerde gebruikers worden verleend om de secties en de tickets te zien. In andere gevallen, kunnen de niet geregistreerde gebruikers de secties en de ticketlijst zien, maar mogen niet de antwoorden zien. Het is het zelfde voor het onderzoekshulpmiddel of memberlijst. Het is aan de administrator te beslissen welk deel van het systeem voor bezoekers toegankelijk is.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="P5"></a><b>Welke secties toegankelijk zijn voor me?</b><br/>
De leden kunnen tot een soort sectie toegang hebben. De openbare secties. Een tweede type is zichtbaar slechts voor de staffleden en de beheerders. In een openbaar sectie, alle elementen en posten door de leden zichtbaar zijn. Merk op dat een sectie kan worden gesloten. Dit betekent dat het nog toegankelijk is, maar de verdere posten zijn niet meer mogelijk.
<br/>
<a href="#top" class="small">Terug naar boven</a><br/><br/>

<a id="F6"></a><b>Wat zijn gesloten posten?</b><br/>
De gesloten tickets worden geplaatst deze manier omdat de actie rond is. U kunt op gesloten posten antwoorden niet.<br/>
