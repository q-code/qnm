Uw profiel (als u wordt geregistreerd) wordt opgeslagen in het gegevensbestand. Om te veranderen klikken hen de verbinding van het Profiel (of op uw gebruikersbenaming in om het even welke lijstpagina). Dit zal u toestaan om al uw informatie te veranderen.
