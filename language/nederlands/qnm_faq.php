<a id="P1"></a><b>Hoe post ik een bericht in een sectie ?</b><br/>
Gemakkelijk. Selecteert de relevante element in de sectieschermen en klik "Post toevoegen", of Kick op de element en dan "post toevoegen". U kunt moeten registreren alvorens u een bericht kunt posten.
<br/>
