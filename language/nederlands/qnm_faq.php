<a id="S3"></a><b>Hoe verander ik mijn rang?</b><br/>
U kunt de verwoording van om het even welke rang (de rangen lijken naast uw gebruikersbenaming in memberlist en de blaasbalg uw naam in de berichtpagina's) veranderen niet. Dit systeem gebruik rangschikt om bepaalde rollen te identificeren. Bijvoorbeeld, hebben de staffleden en de beheerders speciale rollen. Het is aan een beheerder een lid aan de staffleden rang te bevorderen. Een stafflid heeft het recht op dicht/een ticket heropenen, om een ticket te verplaatsen naar de correcte sectie, en de inhoud te wijzigen. Een beheerder kan deze acties (en veel meer) ook uitvoeren. 
<br/>
