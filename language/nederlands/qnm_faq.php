<b>Een korte introductie</b><br/>
<div style="margin:20px 15px">
<a id="I1"></a><b>Wat is een netwerk element?</b><br/>
Een element is een inrichting of apparaat in het netwerk. Merk op dat een element kan ook een groep van apparatuur zoals een station, een rek... Per definitie een element kan subelementen hebben.<br/>
<br/>
<a id="I2"></a><b>Wat is een netwerk connector?</b><br/>
De connector maakt het mogelijk het koppelen van elementen samen. Merk op dat connectors altijd in een element en dat zij niet subelementen. Het is niet verplicht om alle aansluitingen bepalen in de elementen kunnen het netwerk op te bouwen. QNM kunt u het defini&euml;ren van de betrekkingen tussen de apparatuur zonder het gaan naar het niveau van de connectoren.<br/>
<br/>
<a id="I3"></a><b>Wat is een netwerk lijn?</b><br/>
Een lijn is een element (kabel, route, pijp) die kunnen bevatten dubbele connectoren: een aan elke kant van de lijn. Bij het creÃ«ren van connectors op een lijn, zal QNM voorstellen automatisch de bijbehorende aansluitingen (genaamd spiegels) aan het einde van de lijn. Een lijn (bijvoorbeeld een kabel) kunt koppelen apparatuur samen. Het is mogelijk, maar niet verplicht, de relaties beschrijven op het niveau van de aansluitingen (in de apparatuur en aan beide einden van de lijn).<br/>
<br/>
<a id="I4"></a><b>Newterk beheer</b><br/>
QNM maakt beheer van het netwerk (elementen, connectors, lijnen en hun relaties). Merk op dat QNM focus op een gezamenlijk beheer van deze elementen. Ze kunnen documenten (foto's, technische fiches, ...) et QNM maakt het beheer van de noten (waarnemingen) problemen door de gebruikers.<br/>
