<a id="S2"></a><b>Hoe toon ik een beeld met mijn gebruikersbenaming?</b><br/>
Er kan &eacute;&eacute;n beeld (foto) zijn onder een gebruikersbenaming wanneer het bekijken van posten. Dit beeld kan ook een avatar zijn. Het is aan de administrator fotos toe te laten. Als u foto van de pagina van het Profiel niet kunt toevoegen, is het omdat de beheerder het, zeker om goede redenen beslist.
<br/>
