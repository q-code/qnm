<b>A brief introduction</b><br/>
<div style="margin:20px 15px">
<a id="I1"></a><b>What is a network element?</b><br/>
An element is a device or equipment included in the network. Note that a element can also be a group of equipments like a station, a rack... 
By definition an element can contain sub-elements.<br/>
<br/>
<a id="I2"></a><b>What is a network connector?</b><br/>
The connector allows linking elements together.
Note that connectors are always inside an element and that they cannot have sub-elements.
It is not mandatory to define all connectors inside the elements to be able to build the network. 
QNM allows you defining the relations between the equipments without going to the level of the connectors.<br/>
<br/>
<a id="I3"></a><b>What is a network line?</b><br/>
A line is an element (cable, route, pipe) that can contain double connectors : one on each side of the line.
During the creation of connectors on a line, QNM will propose to create automatically the corresponding connectors (called mirrors) at the end of the line.
A line (e.g. a cable) allows linking equipments together. It is possible, but not mandatory, to describe the relations at the level of the connectors (inside the equipments and at both ends of the line).<br/>
<br/>
<a id="I4"></a><b>Network management</b><br/>
QNM allows managing the network (elements, connectors, lines and their relations). 
Note that QNM focus on a collaborative management of these elements. 
They can be documented (pictures, technical fiches...) and QNM allows managing the notes (observations) issued by the users.<br/>
