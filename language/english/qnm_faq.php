<a id="L5"></a><b>I registered in the past but cannot log in anymore!</b><br/>
The most likely reasons for this are: you entered an incorrect username or password (check the email you were sent when you first registered) or the administrator has deleted your account for some reason. If it is the latter case then perhaps you did not use the application? It is usual for boards to periodically remove users who did not use the system. Try registering again and get involved in the application.
<br/>
<a href="#top" class="small">Back to top</a><br/><br/>

<a id="L6"></a><b>Why can't I log in?</b><br/>
Have you registered? Seriously, you must register in order to log in. Have you been banned from the board? (A message will be displayed if you have.) If so, you should contact the webmaster or board administrator to find out why. If you have registered and are not banned and you still cannot log in then check and double-check your username and password. Usually this is the problem; if not, contact the board administrator.
