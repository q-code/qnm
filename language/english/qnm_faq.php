<a id="L1"></a><b>Why registering?</b><br/>
You may not have to. It is up to the administrator of the board as to whether you need to register in order to view, search or post notes. It only takes a few minutes to register so it is recommended you do so.
<br/>
<a href="#top" class="small">Back to top</a><br/><br/>

<a id="L2"></a><b>How registering?</b><br/>
In the registration page you will find the usage policy. Read it carefully. If you agree with the terms and conditions, you can choose your username. Depending on the registration security defined by the Administrator you will have to directly choose your password and confirm it, OR enter your e-mail address (a temporary password will be send to you).
You will also have to copy a code displayed in the form.
If the username you choose is already used by a board member, you will have to choose an other username.
<br/>
<a href="#top" class="small">Back to top</a><br/><br/>

<a id="L3"></a><b>Can I hide my E-mail?</b><br/>
In your profile you will find a privacy option allowing you to hide your e-mail for unregistered visitors or for even for users. Note that staff members will nevertheless will be able to see hidden e-mails.
<br/>
<a href="#top" class="small">Back to top</a><br/><br/>

<a id="L4"></a><b>Lost password!</b><br/>
Don't panic! While your password cannot be retrieved it can be reset. Go to the login page and click Forgotten password. Follow the instructions and you should be back online in no time.
