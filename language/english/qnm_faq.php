<a id="S3"></a><b>How do I change my rank?</b><br/>
You cannot change the wording of any rank (ranks appear next to your username in the memberlist and bellow your name in the message pages). This board use ranks to identify certain roles. For example, staff members and administrators have special roles. It's up to an administrator to promote a member to the staff rank. A staff member has the right to close/re-open a element, to move a element to the correct section, and to modify the content. An administrator can also perform these actions (and much more). 
