<a id="P1"></a><b>Why can't I access the sections?</b><br/>
The board administrator can decide to limit the access for unregistered users. In this case, only registered users will be granted to see the sections, the elements and the notes. In other cases, unregistered users can see the sections and the elements list, but are not allowed to see or issue notes. It's the same for the search tool or the userlist. It's up to the board administrator to decide which part of the board is accessible for visitors.
<br/>
<a href="#top" class="small">Back to top</a><br/><br/>

<a id="P2"></a><b>Which sections are accessible to me?</b><br/>
The board support two kind of sections: the public sections and the hidden sections. Hidden section are visible only by the staff members, while public sections are accessible to the users. It's up to the Administrator to decide if the public section can be visible also by visitors or not. Note that a section can be closed. This means that it is still accessible, but elements and notes cannot be changed (except by the staff members).
