<a id="U1"></a><b>What are Staff members?</b><br/>
Staff members are people assigned the highest level of control over the data. 
These people can edit or delete elements and notes, activate/inactivate, move, or delete elements in all sections. 
<br/>
<a href="#top" class="small">Back to top</a><br/><br/>

<a id="U2"></a><b>What are Administrators?</b><br/>
Administrators are people assigned the highest level of control over the entire board. 
These people can control all facets of board operation which include setting permissions, creating staff members, controlling the board security, style, etc. 
They also have full capabilities of the staff members, in all sections.
<br/>
<a href="#top" class="small">Back to top</a><br/><br/>

<a id="U3"></a><b>What is a Section coordinator?</b><br/>
A section coordinator is a staff member whose job is to look after the running of a section from day to day. Like the staff members, he has the power to edit or delete elements and notes, activate/inactivate, move, or delete elements in the section he is responsible for. Generally coordinators are there to help users.
